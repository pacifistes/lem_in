#include "lem_in.h"

int	ft_isnumber(char *str)
{
	int i;

	i = 0;
	while (str[i] && ((str[i] >= '0' && str[i] <= '9') || (str[i] == '-' &&
	str[i + 1] && str[i + 1] > '0' && str[i + 1] <= '9' && i == 0)))
		i++;
	if (str[i] == '\0' && i == 0)
		return (0);
	return ((str[i] == '\0') ? 1 : 0);
}
